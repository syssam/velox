// FeatureEnabled reports if the given feature name is enabled.
// Logs a warning for unknown feature names to aid debugging mistyped names.
//
// For type-safe feature checks, prefer FeatureEnabledF which accepts a Feature value.
func (g *JenniferGenerator) FeatureEnabled(name string) bool {
	enabled, err := g.graph.FeatureEnabled(name)
	if err != nil {
		slog.Warn("unknown feature name queried", "feature", name, "error", err)
	}
	return enabled
}

// FeatureEnabledF reports if the given feature is enabled.
// This is the type-safe alternative to FeatureEnabled(name string).
func (g *JenniferGenerator) FeatureEnabledF(f Feature) bool {
	return g.graph.featureEnabled(f)
}

// RootPkg returns the root generated package path for per-entity packages.
// Used as the import path for shared types (Value, Hook, etc.) in the root package.
func (g *JenniferGenerator) RootPkg() string {
	if _, ok := g.dialect.(EntityPackageDialect); ok && g.graph.Config != nil && g.graph.Package != "" {
		return g.graph.Package
	}
	return ""
}

// SharedEntityPkg returns the import path for the shared entity/ hub package.
