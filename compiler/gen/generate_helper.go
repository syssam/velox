// LeafPkgPath returns the full import path for an entity's subpackage.
func (g *JenniferGenerator) LeafPkgPath(t *Type) string {
	if g.graph.Config != nil && g.graph.Package != "" {
		return g.graph.Package + "/" + t.PackageDir()
	}
	return g.pkg + "/" + t.PackageDir()
}

// EdgeRelType returns the sqlgraph relationship type constant name for an edge.
func (g *JenniferGenerator) EdgeRelType(e *Edge) string {
	switch e.Rel.Type {
	case O2O:
		return "O2O"
	case O2M:
		return "O2M"
	case M2O:
		return "M2O"
	case M2M:
		return "M2M"
	default:
		return "O2M"
	}
}

