func (m *mockHelper) NewFile(pkg string) *jen.File {
	f := jen.NewFile(pkg)
	f.HeaderComment("Code generated by velox. DO NOT EDIT.")
	return f
}

func (m *mockHelper) GoType(f *gen.Field) jen.Code {
	if f == nil || f.Type == nil {
		return jen.Any()
	}
	if f.Nillable {
		return jen.Op("*").Add(m.BaseType(f))
	}
	return m.BaseType(f)
}

func (m *mockHelper) BaseType(f *gen.Field) jen.Code {
	if f == nil || f.Type == nil {
		return jen.Any()
	}
	switch f.Type.Type {
	case field.TypeString:
		return jen.String()
	case field.TypeInt:
		return jen.Int()
	case field.TypeInt8:
		return jen.Int8()
	case field.TypeInt16:
		return jen.Int16()
	case field.TypeInt32:
		return jen.Int32()
	case field.TypeInt64:
		return jen.Int64()
	case field.TypeUint:
		return jen.Uint()
	case field.TypeUint8:
		return jen.Uint8()
	case field.TypeUint16:
		return jen.Uint16()
	case field.TypeUint32:
		return jen.Uint32()
	case field.TypeUint64:
		return jen.Uint64()
	case field.TypeFloat32:
		return jen.Float32()
	case field.TypeFloat64:
		return jen.Float64()
	case field.TypeBool:
		return jen.Bool()
	case field.TypeTime:
		return jen.Qual("time", "Time")
	case field.TypeUUID:
		return jen.Qual("github.com/google/uuid", "UUID")
	case field.TypeBytes:
		return jen.Index().Byte()
	case field.TypeEnum:
		return jen.String()
	case field.TypeJSON:
		return jen.Any()
	default:
		return jen.Any()
	}
}

func (m *mockHelper) IDType(t *gen.Type) jen.Code {
	if t == nil || t.ID == nil {
		return jen.Int()
	}
	return m.BaseType(t.ID)
}

func (m *mockHelper) ZeroValue(f *gen.Field) jen.Code {
	if f == nil {
		return jen.Lit(0)
	}
	if f.Nillable {
		return jen.Nil()
	}
	if f.Type == nil {
		return jen.Lit(0)
	}
	switch f.Type.Type {
	case field.TypeString, field.TypeEnum:
		return jen.Lit("")
	case field.TypeInt, field.TypeInt8, field.TypeInt16, field.TypeInt32, field.TypeInt64,
		field.TypeUint, field.TypeUint8, field.TypeUint16, field.TypeUint32, field.TypeUint64:
		return jen.Lit(0)
	case field.TypeFloat32, field.TypeFloat64:
		return jen.Lit(0.0)
	case field.TypeBool:
		return jen.False()
	default:
		return jen.Nil()
	}
}

