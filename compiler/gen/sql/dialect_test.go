// TestDialectSupportsFeature tests the SupportsFeature method.
func TestDialectSupportsFeature(t *testing.T) {
	t.Parallel()
	helper := newMockHelper()
	dialect := NewDialect(helper)

	tests := []struct {
		feature  string
		expected bool
	}{
		{"migrate", false}, // handled by MigrateGenerator, not FeatureGenerator
		{"upsert", true},
		{"lock", true},
		{"modifier", true},
		{"hook", true},
		// intercept and privacy are handled by OptionalFeatureGenerator,
		// not by SupportsFeature/GenFeature.
		{"intercept", false},
		{"privacy", false},
		{"unknown_feature", false},
		{"gremlin", false},
		{"", false},
	}

	for _, tt := range tests {
		t.Run(tt.feature, func(t *testing.T) {
			result := dialect.SupportsFeature(tt.feature)
			assert.Equal(t, tt.expected, result, "feature %q support mismatch", tt.feature)
		})
	}
}

// TestDialectGenFeature tests the GenFeature method.
func TestDialectGenFeature(t *testing.T) {
	t.Parallel()
	helper := newMockHelper()
	dialect := NewDialect(helper)

	tests := []struct {
		feature string
		isNil   bool
	}{
		{"migrate", true},   // Returns nil — handled by MigrateGenerator, not GenFeature
		{"hook", false},     // Now returns non-nil (hook/hook.go)
		{"intercept", true}, // Handled via OptionalFeatureGenerator
		{"privacy", true},   // Handled via OptionalFeatureGenerator
		{"unknown", true},
	}

	for _, tt := range tests {
		t.Run(tt.feature, func(t *testing.T) {
			result, err := dialect.GenFeature(tt.feature)
			require.NoError(t, err)
			if tt.isNil {
				assert.Nil(t, result)
			} else {
				assert.NotNil(t, result)
			}
		})
	}
}

