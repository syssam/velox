func TestCollectTypedJSONScalars_CollectsTypedJSON(t *testing.T) {
	g := &Generator{
		config: Config{ORMPackage: "example.com/ent"},
		graph: &gen.Graph{
			Nodes: []*gen.Type{
				{
					Name: "User",
					Fields: []*gen.Field{
						{
							Name: "address",
							Type: &field.TypeInfo{
								Type:    field.TypeJSON,
								Ident:   "schema.Address",
								PkgPath: "example.com/schema",
								RType: &field.RType{
									Name:    "Address",
									Ident:   "schema.Address",
									Kind:    reflect.Struct,
									PkgPath: "example.com/schema",
								},
							},
						},
					},
				},
			},
		},
	}
	scalars := g.collectTypedJSONScalars()
	require.Len(t, scalars, 1)
	s, ok := scalars["example.com/schema.Address"]
	require.True(t, ok)
	assert.Equal(t, "Address", s.ScalarName)
	assert.Equal(t, "example.com/ent.Address", s.GoType)
	assert.Equal(t, "example.com/schema", s.OrigPkgPath)
	assert.Equal(t, "Address", s.TypeName)
}

