func TestGenScalars_EmptyProducesFile(t *testing.T) {
	g := &Generator{
		config:           Config{Package: "test"},
		typedJSONScalars: map[string]typedJSONScalar{},
	}
	f := g.genScalars()
	require.NotNil(t, f)
	var buf bytes.Buffer
	err := f.Render(&buf)
	require.NoError(t, err)
	assert.Contains(t, buf.String(), "Code generated by velox. DO NOT EDIT.")
	assert.Contains(t, buf.String(), "package test")
}

func TestGenScalars_WithScalar(t *testing.T) {
	g := &Generator{
		config: Config{Package: "test", ORMPackage: "example.com/ent"},
		typedJSONScalars: map[string]typedJSONScalar{
			"example.com/schema.Address": {
				ScalarName:  "Address",
				GoType:      "example.com/ent.Address",
				OrigPkgPath: "example.com/schema",
				TypeName:    "Address",
			},
		},
	}
	f := g.genScalars()
	require.NotNil(t, f)
	var buf bytes.Buffer
	err := f.Render(&buf)
	require.NoError(t, err)
	code := buf.String()
	assert.Contains(t, code, "MarshalAddress")
	assert.Contains(t, code, "UnmarshalAddress")
	assert.Contains(t, code, "MarshalAddressPtr")
	assert.Contains(t, code, "UnmarshalAddressPtr")
	// Should contain the type alias
	assert.Contains(t, code, "Address =")
}

func TestGenScalars_MultipleScalarsSorted(t *testing.T) {
	g := &Generator{
		config: Config{Package: "test", ORMPackage: "example.com/ent"},
		typedJSONScalars: map[string]typedJSONScalar{
			"example.com/schema.Coordinate": {
				ScalarName:  "Coordinate",
				GoType:      "example.com/ent.Coordinate",
				OrigPkgPath: "example.com/schema",
				TypeName:    "Coordinate",
			},
			"example.com/schema.Address": {
				ScalarName:  "Address",
				GoType:      "example.com/ent.Address",
				OrigPkgPath: "example.com/schema",
				TypeName:    "Address",
			},
		},
	}
	f := g.genScalars()
	require.NotNil(t, f)
	var buf bytes.Buffer
	err := f.Render(&buf)
	require.NoError(t, err)
	code := buf.String()
	// Both scalars should be present
	assert.Contains(t, code, "MarshalAddress")
	assert.Contains(t, code, "MarshalCoordinate")
	// Address should appear before Coordinate (sorted by key)
	addrIdx := bytes.Index([]byte(code), []byte("MarshalAddress"))
	coordIdx := bytes.Index([]byte(code), []byte("MarshalCoordinate"))
	assert.Less(t, addrIdx, coordIdx, "Address should appear before Coordinate (sorted)")
}
