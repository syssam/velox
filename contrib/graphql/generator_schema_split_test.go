func TestGenerator_WriteSchema_SkipsUnchanged(t *testing.T) {
	tmpDir, err := os.MkdirTemp("", "graphql-incremental-*")
	if err != nil {
		t.Fatalf("failed to create temp dir: %v", err)
	}
	defer os.RemoveAll(tmpDir)

	g := mockGraph()
	gen := NewGenerator(g, Config{
		OutDir:          tmpDir,
		Package:         "graphql",
		SchemaGenerator: true,
		ORMPackage:      "example/ent",
		RelayConnection: true,
		WhereInputs:     true,
		Mutations:       true,
		Ordering:        true,
		RelaySpec:       true,
	})

	content := "# Test schema\ntype Query { hello: String }\n"

	// First write
	if writeErr := gen.writeSchema(context.Background(), content, "", "test.graphql"); writeErr != nil {
		t.Fatalf("first writeSchema() error = %v", writeErr)
	}

	path := filepath.Join(tmpDir, "test.graphql")
	info1, err := os.Stat(path)
	if err != nil {
		t.Fatalf("file not created: %v", err)
	}
	mtime1 := info1.ModTime()

	// Ensure filesystem can detect time difference
	time.Sleep(50 * time.Millisecond)

	// Second write with same content — should be skipped
	if writeErr := gen.writeSchema(context.Background(), content, "", "test.graphql"); writeErr != nil {
		t.Fatalf("second writeSchema() error = %v", writeErr)
	}

	info2, err := os.Stat(path)
	if err != nil {
		t.Fatalf("file not found after second write: %v", err)
	}
	mtime2 := info2.ModTime()

	if !mtime1.Equal(mtime2) {
		t.Errorf("mtime changed on unchanged content: %v -> %v", mtime1, mtime2)
	}

	// Third write with different content — should update
	newContent := "# Updated schema\ntype Query { world: String }\n"
	if writeErr := gen.writeSchema(context.Background(), newContent, "", "test.graphql"); writeErr != nil {
		t.Fatalf("third writeSchema() error = %v", writeErr)
	}

	written, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("failed to read file: %v", err)
	}
	if string(written) != newContent {
		t.Errorf("file content not updated: got %q, want %q", string(written), newContent)
	}
}
