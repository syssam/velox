func TestGenerator_Generate_SplitPerEntity(t *testing.T) {
	tmpDir, err := os.MkdirTemp("", "graphql-split-test-*")
	if err != nil {
		t.Fatalf("failed to create temp dir: %v", err)
	}
	defer os.RemoveAll(tmpDir)

	g := mockGraph()
	gen := NewGenerator(g, Config{
		OutDir:          tmpDir,
		Package:         "graphql",
		RelayConnection: true,
		WhereInputs:     true,
		Mutations:       true,
		Ordering:        true,
		RelaySpec:       true,
		SchemaGenerator: true,
		SchemaSplitMode: SchemaSplitPerEntity,
		ORMPackage:      "example/ent",
	})

	if genErr := gen.Generate(context.Background()); genErr != nil {
		t.Fatalf("Generate() error = %v", genErr)
	}

	// Root file should exist
	rootPath := filepath.Join(tmpDir, "schema.graphql")
	rootContent, err := os.ReadFile(rootPath)
	if err != nil {
		t.Fatalf("root schema file not created: %v", err)
	}
	rootStr := string(rootContent)

	// Root should contain shared types only (no entity types or enums)
	for _, want := range []string{
		"directive @goField", "scalar Cursor", "interface Node",
		"enum OrderDirection", "type PageInfo", "type Query", "type Mutation",
	} {
		if !strings.Contains(rootStr, want) {
			t.Errorf("root schema should contain %q", want)
		}
	}

	// Root should NOT contain entity types, enums, or entity-specific type definitions
	// Note: type references (e.g., ": UserConnection!" in Query) are expected
	for _, notwant := range []string{
		"type User implements", "type Post implements", "enum UserStatus",
		"input UserWhereInput", "input CreateUserInput", "input UpdateUserInput",
		"input UserOrder ", "enum UserOrderField", "type UserConnection", "type UserEdge",
	} {
		if strings.Contains(rootStr, notwant) {
			t.Errorf("root schema should NOT contain type definition %q (belongs in per-entity file)", notwant)
		}
	}

	// Per-entity files should exist (mockGraph has User and Post only)
	for _, entity := range []string{"user", "post"} {
		entityPath := filepath.Join(tmpDir, fmt.Sprintf("velox_%s.graphql", entity))
		content, readErr := os.ReadFile(entityPath)
		if readErr != nil {
			t.Errorf("entity file velox_%s.graphql not created: %v", entity, readErr)
			continue
		}
		contentStr := string(content)

		if !strings.Contains(contentStr, "Code generated by velox") {
			t.Errorf("velox_%s.graphql should contain generated header", entity)
		}
	}

	// Verify User entity file has WhereInput + mutation inputs
	userContent, err := os.ReadFile(filepath.Join(tmpDir, "velox_user.graphql"))
	if err != nil {
		t.Fatalf("velox_user.graphql not readable: %v", err)
	}
	userStr := string(userContent)

	for _, want := range []string{
		"type User", "enum UserStatus",
		"UserWhereInput", "CreateUserInput", "UpdateUserInput",
		"UserOrder", "UserOrderField", "UserConnection", "UserEdge",
	} {
		if !strings.Contains(userStr, want) {
			t.Errorf("velox_user.graphql should contain %q", want)
		}
	}

	// User file should NOT contain shared types
	for _, notwant := range []string{
		"type Query", "scalar Cursor", "enum OrderDirection",
	} {
		if strings.Contains(userStr, notwant) {
			t.Errorf("velox_user.graphql should NOT contain %q", notwant)
		}
	}

	// Go files should still be generated (shared + per-entity)
	goFiles := []string{
		"gql_node.go",
		// gqlfilter/ sub-package
		"gqlfilter/filter.go",
		"gqlfilter/user.go", "gqlfilter/post.go",
		// Per-entity files (entity sub-packages)
		"user/gql_node.go", "post/gql_node.go",
		"user/gql_collection.go", "post/gql_collection.go",
		"query/gql_pagination_user.go", "query/gql_pagination_post.go",
		"user/gql_mutation_input.go", "post/gql_mutation_input.go",
	}
	for _, goFile := range goFiles {
		if _, statErr := os.Stat(filepath.Join(tmpDir, goFile)); os.IsNotExist(statErr) {
			t.Errorf("expected Go file %q was not created", goFile)
		}
	}
}

