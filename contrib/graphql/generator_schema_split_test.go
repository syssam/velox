func TestSchemaSplitMode_Constants(t *testing.T) {
	var mode SchemaSplitMode
	if mode != SchemaSplitNone {
		t.Errorf("zero value should be SchemaSplitNone, got %d", mode)
	}
	if SchemaSplitNone != 0 {
		t.Errorf("SchemaSplitNone should be 0, got %d", SchemaSplitNone)
	}
	if SchemaSplitByCategory != 1 {
		t.Errorf("SchemaSplitByCategory should be 1, got %d", SchemaSplitByCategory)
	}
	if SchemaSplitPerEntity != 2 {
		t.Errorf("SchemaSplitPerEntity should be 2, got %d", SchemaSplitPerEntity)
	}
}

