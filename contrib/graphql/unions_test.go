func TestGenerate_SingleMemberUnion_ReturnsError(t *testing.T) {
	g := mockGraph()
	giveOwnAnnotations(g)
	// Only User declares membership in "Solo" — Post does not.
	userAnn := extractGraphQLAnnotation(g.Nodes[0].Annotations)
	userAnn.Unions = []string{"Solo"}
	g.Nodes[0].Annotations[AnnotationName] = userAnn

	tmpDir := t.TempDir()
	gen := NewGenerator(g, Config{
		OutDir:     tmpDir,
		Package:    "graphql",
		ORMPackage: "example/ent",
	})
	err := gen.Generate(context.Background())
	require.Error(t, err)
	assert.Contains(t, err.Error(), `union "Solo"`)
}

func TestGenUnionsShared_DeterministicOrder(t *testing.T) {
	g := newTestGenerator(
		unionType("Zebra", "Z"),
		unionType("Apple", "A"),
	)
	members := map[string][]string{"A": {"Apple", "Zebra"}, "Z": {"Apple", "Zebra"}}
	out1 := g.genUnionsShared(members).GoString()
	out2 := g.genUnionsShared(members).GoString()
	assert.Equal(t, out1, out2, "output must be deterministic across runs")

	// Apple's methods come before Zebra's in the file (entities sorted alphabetically).
	appleIdx := strings.Index(out1, "func (*Apple)")
	zebraIdx := strings.Index(out1, "func (*Zebra)")
	require.GreaterOrEqual(t, appleIdx, 0)
	require.GreaterOrEqual(t, zebraIdx, 0)
	assert.Less(t, appleIdx, zebraIdx)
}
