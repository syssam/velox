// --- Annotation: UnionMember constructor / merge / getter ---

func TestUnionMember_Constructor(t *testing.T) {
	ann := UnionMember("SearchResult", "Activity")
	assert.Equal(t, []string{"SearchResult", "Activity"}, ann.Unions)
	assert.Equal(t, []string{"SearchResult", "Activity"}, ann.GetUnions())
}

func TestUnionMember_EmptyConstructor(t *testing.T) {
	ann := UnionMember()
	assert.Empty(t, ann.Unions)
}

func TestMergeAnnotations_Unions_Deduplicates(t *testing.T) {
	a := UnionMember("SearchResult", "Activity")
	b := UnionMember("Activity", "Feed") // "Activity" duplicate
	result := MergeAnnotations(a, b)
	assert.Equal(t, []string{"SearchResult", "Activity", "Feed"}, result.Unions,
		"Unions should append-deduplicate across merge")
}

func TestMerge_Unions_Deduplicates(t *testing.T) {
	a := UnionMember("X")
	b := UnionMember("X", "Y")
	result := a.Merge(b).(Annotation)
	assert.Equal(t, []string{"X", "Y"}, result.Unions)
}

// --- Validator ---

func unionType(name string, unions ...string) *entgen.Type {
	return &entgen.Type{
		Name: name,
		ID: &entgen.Field{
			Name: "id",
			Type: &field.TypeInfo{Type: field.TypeInt64},
		},
		Annotations: map[string]any{
			AnnotationName: UnionMember(unions...),
		},
	}
}

func TestValidateUnions_RequiresAtLeastTwoMembers(t *testing.T) {
	g := newTestGenerator(unionType("User", "SearchResult"))
	_, err := g.validateUnions()
	require.Error(t, err)
	assert.Contains(t, err.Error(), `union "SearchResult"`)
	assert.Contains(t, err.Error(), "at least 2")
}

func TestValidateUnions_RejectsEmptyName(t *testing.T) {
	g := newTestGenerator(unionType("User", ""))
	_, err := g.validateUnions()
	require.Error(t, err)
	assert.Contains(t, err.Error(), "empty union name")
}

func TestValidateUnions_AcceptsMultipleMembers(t *testing.T) {
	g := newTestGenerator(
		unionType("User", "SearchResult"),
		unionType("Post", "SearchResult"),
		unionType("Comment", "SearchResult", "Activity"),
	)
	// Single-member "Activity" should fail.
	_, err := g.validateUnions()
	require.Error(t, err)
	assert.Contains(t, err.Error(), `union "Activity"`)
}

func TestValidateUnions_HappyPath(t *testing.T) {
	g := newTestGenerator(
		unionType("User", "SearchResult", "Activity"),
		unionType("Post", "SearchResult", "Activity"),
	)
	members, err := g.validateUnions()
	require.NoError(t, err)
	assert.Equal(t, []string{"Post", "User"}, members["SearchResult"], "members are sorted")
	assert.Equal(t, []string{"Post", "User"}, members["Activity"])
}

func TestValidateUnions_NoAnnotations(t *testing.T) {
	// Entities without UnionMember should produce empty map, no error.
	g := newTestGenerator(
		&entgen.Type{Name: "User", ID: &entgen.Field{Name: "id", Type: &field.TypeInfo{Type: field.TypeInt64}}},
	)
	members, err := g.validateUnions()
	require.NoError(t, err)
	assert.Empty(t, members)
}

