package graphql

// Pagination generator: generates per-entity Paginate method on root query wrapper.
// Generates clean, direct pagination like Ent — no callback indirection.
// Also contains shared helpers for Relay cursor-based pagination code generation.

import (
	"fmt"
	"math"
	"strings"

	"github.com/dave/jennifer/jen"

	"github.com/syssam/velox/compiler/gen"
)

const gqlrelayPkg = "github.com/syssam/velox/contrib/graphql/gqlrelay"

// genEntityPagination generates a Paginate method on the query/ package query type.
// Direct style (like Ent): apply order → apply cursors → count → fetch → build connection.
// Pagination types (Connection, Edge, Order) are referenced from entity/ package.
func (g *Generator) genEntityPagination(t *gen.Type) *jen.File {
	if g.config.ORMPackage == "" {
		return nil
	}

	queryPkg := g.config.ORMPackage + "/query"
	entityPkg := g.config.ORMPackage + "/entity"
	subEntityPkg := g.entityPkgPath(t)

	f := jen.NewFilePathName(queryPkg, "query")
	f.HeaderComment("Code generated by velox. DO NOT EDIT.")

	f.ImportName("context", "context")
	f.ImportName("fmt", "fmt")
	f.ImportName(gqlrelayPkg, "gqlrelay")
	f.ImportName("github.com/syssam/velox/dialect/sql", "sql")
	f.ImportName(entityPkg, "entity")
	f.ImportName(subEntityPkg, g.entityPkgName(t))

	typeName := g.graphqlTypeName(t)
	queryName := t.QueryName()

	// Pagination types are now in entity/ package.
	connName := typeName + "Connection"
	edgeName := typeName + "Edge"
	optName := typeName + "PaginateOption"
	pagerCfg := typeName + "PagerConfig"

	idField := "ID"
	idColumn := "FieldID"
	if t.ID != nil && t.ID.Name != "" {
		if t.ID.Name != "id" {
			idField = pascal(t.ID.Name)
		}
		idColumn = "Field" + idField
	}

	// Paginate method on query.UserQuery — direct style like Ent.
	f.Commentf("Paginate returns a paginated %s using Relay cursor pagination.", connName)
	f.Func().Params(
		jen.Id("q").Op("*").Id(queryName),
	).Id("Paginate").Params(
		jen.Id("ctx").Qual("context", "Context"),
		jen.Id("after").Op("*").Qual(gqlrelayPkg, "Cursor"),
		jen.Id("first").Op("*").Int(),
		jen.Id("before").Op("*").Qual(gqlrelayPkg, "Cursor"),
		jen.Id("last").Op("*").Int(),
		jen.Id("opts").Op("...").Qual(entityPkg, optName),
	).Params(
		jen.Op("*").Qual(entityPkg, connName),
		jen.Error(),
	).BlockFunc(func(grp *jen.Group) {
		// Validate
		grp.If(jen.Err().Op(":=").Qual(gqlrelayPkg, "ValidateFirstLast").Call(jen.Id("first"), jen.Id("last")), jen.Err().Op("!=").Nil()).Block(
			jen.Return(jen.Nil(), jen.Err()),
		)

		// Apply options
		grp.Id("cfg").Op(":=").Op("&").Qual(entityPkg, pagerCfg).Values()
		grp.For(jen.List(jen.Id("_"), jen.Id("opt")).Op(":=").Range().Id("opts")).Block(
			jen.Id("opt").Call(jen.Id("cfg")),
		)

		// Apply filter
		grp.If(jen.Id("cfg").Dot("Filter").Op("!=").Nil()).BlockFunc(func(filterGrp *jen.Group) {
			filterGrp.List(jen.Id("f"), jen.Id("ok")).Op(":=").Id("cfg").Dot("Filter").Op(".").Parens(
				jen.Func().Params(jen.Op("*").Id(queryName)).Params(jen.Op("*").Id(queryName), jen.Error()),
			)
			filterGrp.If(jen.Op("!").Id("ok")).Block(
				jen.Return(jen.Nil(), jen.Qual("fmt", "Errorf").Call(
					jen.Lit("pagination: invalid filter type %T"), jen.Id("cfg").Dot("Filter"),
				)),
			)
			filterGrp.Var().Id("err").Error()
			filterGrp.List(jen.Id("q"), jen.Id("err")).Op("=").Id("f").Call(jen.Id("q"))
			filterGrp.If(jen.Id("err").Op("!=").Nil()).Block(
				jen.Return(jen.Nil(), jen.Id("err")),
			)
		})

		// Apply order — use query builder methods directly (same package).
		grp.If(jen.Id("cfg").Dot("Order").Op("!=").Nil().Op("&&").Id("cfg").Dot("Order").Dot("Field").Op("!=").Nil()).BlockFunc(func(orderGrp *jen.Group) {
			orderGrp.Id("q").Dot("Order").Call(
				jen.Qual("github.com/syssam/velox/dialect/sql", "OrderByField").Call(
					jen.Id("cfg").Dot("Order").Dot("Field").Dot("Column"),
					jen.Qual(gqlrelayPkg, "DirectionOrderTerm").Call(jen.Id("cfg").Dot("Order").Dot("Direction")),
				).Dot("ToFunc").Call(),
			)
			orderGrp.Id("q").Dot("Order").Call(
				jen.Qual("github.com/syssam/velox/dialect/sql", "OrderByField").Call(
					jen.Qual(subEntityPkg, idColumn),
					jen.Qual(gqlrelayPkg, "DirectionOrderTerm").Call(jen.Id("cfg").Dot("Order").Dot("Direction")),
				).Dot("ToFunc").Call(),
			)
		}).Else().Block(
			jen.Id("q").Dot("Order").Call(
				jen.Qual("github.com/syssam/velox/dialect/sql", "OrderByField").Call(
					jen.Qual(subEntityPkg, idColumn),
				).Dot("ToFunc").Call(),
			),
		)

		// Apply cursors
		grp.If(jen.Id("after").Op("!=").Nil().Op("||").Id("before").Op("!=").Nil()).BlockFunc(func(cursorGrp *jen.Group) {
			cursorGrp.Var().Id("orderField").String()
			cursorGrp.Var().Id("orderDir").Qual(gqlrelayPkg, "OrderDirection")
			cursorGrp.If(jen.Id("cfg").Dot("Order").Op("!=").Nil().Op("&&").Id("cfg").Dot("Order").Dot("Field").Op("!=").Nil()).Block(
				jen.Id("orderField").Op("=").Id("cfg").Dot("Order").Dot("Field").Dot("Column"),
				jen.Id("orderDir").Op("=").Id("cfg").Dot("Order").Dot("Direction"),
			)
			cursorGrp.For(jen.List(jen.Id("_"), jen.Id("p")).Op(":=").Range().Qual(gqlrelayPkg, "CursorsPredicate").Call(
				jen.Id("after"), jen.Id("before"), jen.Qual(subEntityPkg, idColumn), jen.Id("orderField"), jen.Id("orderDir"),
			)).Block(
				jen.Id("q").Dot("Where").Call(jen.Id("p")),
			)
		})

		// Count total (before limit). Clone returns UserQuerier which has Count.
		grp.List(jen.Id("totalCount"), jen.Id("err")).Op(":=").Id("q").Dot("Clone").Call().Dot("Count").Call(jen.Id("ctx"))
		grp.If(jen.Err().Op("!=").Nil()).Block(
			jen.Return(jen.Nil(), jen.Err()),
		)

		// Apply limit
		grp.Id("reverse").Op(":=").Id("last").Op("!=").Nil()
		grp.If(jen.Id("first").Op("!=").Nil()).Block(
			jen.Id("q").Dot("Limit").Call(jen.Op("*").Id("first").Op("+").Lit(1)),
		).Else().If(jen.Id("last").Op("!=").Nil()).Block(
			jen.Id("q").Dot("Limit").Call(jen.Op("*").Id("last").Op("+").Lit(1)),
		)

		// Fetch
		grp.List(jen.Id("nodes"), jen.Id("err")).Op(":=").Id("q").Dot("All").Call(jen.Id("ctx"))
		grp.If(jen.Err().Op("!=").Nil()).Block(
			jen.Return(jen.Nil(), jen.Err()),
		)

		// Build connection
		grp.Id("conn").Op(":=").Op("&").Qual(entityPkg, connName).Values(jen.Dict{
			jen.Id("TotalCount"): jen.Id("totalCount"),
		})

		// Determine hasNext/hasPrev and trim probe row
		grp.Id("limit").Op(":=").Len(jen.Id("nodes"))
		grp.If(jen.Id("first").Op("!=").Nil().Op("&&").Id("limit").Op(">").Op("*").Id("first")).Block(
			jen.Id("nodes").Op("=").Id("nodes").Index(jen.Empty(), jen.Op("*").Id("first")),
			jen.Id("conn").Dot("PageInfo").Dot("HasNextPage").Op("=").True(),
		)
		grp.If(jen.Id("last").Op("!=").Nil().Op("&&").Id("limit").Op(">").Op("*").Id("last")).Block(
			jen.Id("nodes").Op("=").Id("nodes").Index(jen.Id("limit").Op("-").Op("*").Id("last"), jen.Empty()),
			jen.Id("conn").Dot("PageInfo").Dot("HasPreviousPage").Op("=").True(),
		)
		grp.If(jen.Id("before").Op("!=").Nil()).Block(
			jen.Id("conn").Dot("PageInfo").Dot("HasNextPage").Op("=").True(),
		)
		grp.If(jen.Id("after").Op("!=").Nil()).Block(
			jen.Id("conn").Dot("PageInfo").Dot("HasPreviousPage").Op("=").True(),
		)

		// Reverse if paginating backwards
		grp.If(jen.Id("reverse")).Block(
			jen.For(jen.List(jen.Id("i"), jen.Id("j")).Op(":=").Lit(0).Op(",").Len(jen.Id("nodes")).Op("-").Lit(1), jen.Id("i").Op("<").Id("j"), jen.List(jen.Id("i"), jen.Id("j")).Op("=").Id("i").Op("+").Lit(1).Op(",").Id("j").Op("-").Lit(1)).Block(
				jen.List(jen.Id("nodes").Index(jen.Id("i")), jen.Id("nodes").Index(jen.Id("j"))).Op("=").List(jen.Id("nodes").Index(jen.Id("j")), jen.Id("nodes").Index(jen.Id("i"))),
			),
		)

		// Build edges with cursors
		grp.Id("conn").Dot("Edges").Op("=").Make(jen.Index().Op("*").Qual(entityPkg, edgeName), jen.Len(jen.Id("nodes")))
		grp.For(jen.List(jen.Id("i"), jen.Id("node")).Op(":=").Range().Id("nodes")).BlockFunc(func(edgeGrp *jen.Group) {
			// ToCursor
			edgeGrp.Var().Id("cursor").Qual(gqlrelayPkg, "Cursor")
			edgeGrp.If(jen.Id("cfg").Dot("Order").Op("!=").Nil().Op("&&").Id("cfg").Dot("Order").Dot("Field").Op("!=").Nil().Op("&&").Id("cfg").Dot("Order").Dot("Field").Dot("ToCursor").Op("!=").Nil()).Block(
				jen.Id("cursor").Op("=").Id("cfg").Dot("Order").Dot("Field").Dot("ToCursor").Call(jen.Id("node")),
			).Else().Block(
				jen.Id("cursor").Op("=").Qual(gqlrelayPkg, "Cursor").Values(jen.Dict{
					jen.Id("ID"): jen.Id("node").Dot(idField),
				}),
			)
			edgeGrp.Id("conn").Dot("Edges").Index(jen.Id("i")).Op("=").Op("&").Qual(entityPkg, edgeName).Values(jen.Dict{
				jen.Id("Node"):   jen.Id("node"),
				jen.Id("Cursor"): jen.Id("cursor"),
			})
		})

		// Set start/end cursors
		grp.If(jen.Len(jen.Id("conn").Dot("Edges")).Op(">").Lit(0)).Block(
			jen.Id("conn").Dot("PageInfo").Dot("StartCursor").Op("=").Op("&").Id("conn").Dot("Edges").Index(jen.Lit(0)).Dot("Cursor"),
			jen.Id("conn").Dot("PageInfo").Dot("EndCursor").Op("=").Op("&").Id("conn").Dot("Edges").Index(jen.Len(jen.Id("conn").Dot("Edges")).Op("-").Lit(1)).Dot("Cursor"),
		)

		grp.Return(jen.Id("conn"), jen.Nil())
	})

	return f
}

// isOrderableField returns true if the field can be used for ordering.
// Text fields (unbounded size, i.e., Size >= math.MaxInt32) are excluded
// because ordering by large text columns is inefficient and rarely useful.
// Orderable types: String (non-Text), Int, Int64, Float32, Float64, Bool, Enum, Time.
func (g *Generator) isOrderableField(field *gen.Field) bool {
	if field.IsString() {
		col := field.Column()
		// Exclude Text fields (field.Text() sets Size = math.MaxInt32).
		if col.Size >= math.MaxInt32 {
			return false
		}
		return true
	}
	return field.IsTime() || field.IsInt() || field.IsInt64() || field.IsBool() || field.IsEnum() || field.Type.Type.Float()
}

// orderableFields returns the orderable fields for a type, filtering out skipped fields.
func (g *Generator) orderableFields(t *gen.Type) []*gen.Field {
	fields := g.filterFields(t.Fields, SkipOrderField)
	var result []*gen.Field
	for _, fld := range fields {
		if g.isOrderableField(fld) {
			result = append(result, fld)
		}
	}
	return result
}

// typeReceiver generates a receiver name for a type following Ent's convention.
func typeReceiver(name string) string {
	s := toSnakeCase(name)
	parts := strings.Split(s, "_")

	var r strings.Builder
	for _, part := range parts {
		if part != "" {
			r.WriteByte(part[0])
		}
	}

	result := r.String()
	if result == "" {
		return strings.ToLower(name[:1])
	}
	return result
}

// genPaginationShared generates shared pagination type aliases for the entity/ package.
func (g *Generator) genPaginationShared() *jen.File {
	f := jen.NewFilePathName(g.config.ORMPackage+"/entity", "entity")
	f.HeaderComment("Code generated by velox. DO NOT EDIT.")

	f.ImportName(gqlrelayPkg, "gqlrelay")

	f.Comment("Shared pagination types from gqlrelay.")
	f.Type().Defs(
		jen.Id("OrderDirection").Op("=").Qual(gqlrelayPkg, "OrderDirection"),
		jen.Id("Cursor").Op("=").Qual(gqlrelayPkg, "Cursor"),
		jen.Id("PageInfo").Op("=").Qual(gqlrelayPkg, "PageInfo"),
	)

	f.Const().Defs(
		jen.Id("OrderDirectionAsc").Op("=").Qual(gqlrelayPkg, "OrderDirectionAsc"),
		jen.Id("OrderDirectionDesc").Op("=").Qual(gqlrelayPkg, "OrderDirectionDesc"),
	)

	f.Var().Id("ErrInvalidPagination").Op("=").Qual(gqlrelayPkg, "ErrInvalidPagination")

	return f
}

// genModelPaginationTypes generates Connection, Edge, Order, OrderField,
// PagerConfig, and PaginateOption types for all entities into
// gql_pagination.go in the entity/ package.
// Entity types live in entity/ so pagination types reference them directly.
func (g *Generator) genModelPaginationTypes(nodes []*gen.Type) *jen.File {
	if g.config.ORMPackage == "" {
		return nil
	}

	f := jen.NewFilePathName(g.config.ORMPackage+"/entity", "entity")
	f.HeaderComment("Code generated by velox. DO NOT EDIT.")

	// Imports
	f.ImportName("io", "io")
	f.ImportName("strconv", "strconv")
	f.ImportName("fmt", "fmt")
	f.ImportName("github.com/syssam/velox", "velox")
	f.ImportName("github.com/syssam/velox/dialect/sql", "sql")
	f.ImportName(gqlrelayPkg, "gqlrelay")

	// All types reference entity structs directly (same package).
	for _, t := range nodes {
		g.genModelPaginationDefs(f, t)
	}

	// Generate Paginatable interfaces for type assertion in edge methods.
	// Edge methods use q.(UserPaginatable).Paginate(...) since the querier
	// interface doesn't include Paginate (it's added by the graphql extension).
	for _, t := range nodes {
		g.genPaginatableInterface(f, t)
	}

	return f
}

// genModelPaginationDefs generates all pagination type definitions for a single entity.
// Types are local to the root package — entity struct references use jen.Id (same package).
func (g *Generator) genModelPaginationDefs(f *jen.File, t *gen.Type) {
	typeName := g.graphqlTypeName(t)
	receiver := typeReceiver(t.Name)
	multiOrder := g.hasMultiOrder(t)

	edgeName := typeName + "Edge"
	connName := typeName + "Connection"
	pagerConfigName := typeName + "PagerConfig"
	optName := typeName + "PaginateOption"
	orderName := typeName + "Order"
	orderFieldName := typeName + "OrderField"
	defaultOrderName := "Default" + typeName + "Order"

	// --- Edge and Connection: non-generic structs to avoid monomorphization ---
	f.Comment(fmt.Sprintf("%s is the edge representation of %s.", edgeName, typeName))
	f.Type().Id(edgeName).Struct(
		jen.Id("Node").Op("*").Id(typeName).Tag(map[string]string{"json": "node"}),
		jen.Id("Cursor").Qual(gqlrelayPkg, "Cursor").Tag(map[string]string{"json": "cursor"}),
	)
	f.Line()

	f.Comment(fmt.Sprintf("%s is the connection containing edges to %s.", connName, typeName))
	f.Type().Id(connName).Struct(
		jen.Id("Edges").Index().Op("*").Id(edgeName).Tag(map[string]string{"json": "edges"}),
		jen.Id("PageInfo").Qual(gqlrelayPkg, "PageInfo").Tag(map[string]string{"json": "pageInfo"}),
		jen.Id("TotalCount").Int().Tag(map[string]string{"json": "totalCount"}),
	)
	f.Line()

	f.Line()

	// --- PaginateOption type ---
	f.Comment(fmt.Sprintf("%s enables pagination customization.", optName))
	f.Type().Id(optName).Func().Params(jen.Op("*").Id(pagerConfigName))
	f.Line()

	// --- PagerConfig struct ---
	f.Comment(fmt.Sprintf("%s holds pagination configuration for %s.", pagerConfigName, typeName))
	if multiOrder {
		f.Type().Id(pagerConfigName).Struct(
			jen.Id("Order").Index().Op("*").Id(orderName),
			jen.Comment("Filter is a typed filter function stored as any to avoid circular imports."),
			jen.Comment("The actual type is func(*XxxQuery) (*XxxQuery, error)."),
			jen.Id("Filter").Any(),
		)
	} else {
		f.Type().Id(pagerConfigName).Struct(
			jen.Id("Order").Op("*").Id(orderName),
			jen.Comment("Filter is a typed filter function stored as any to avoid circular imports."),
			jen.Comment("The actual type is func(*XxxQuery) (*XxxQuery, error)."),
			jen.Id("Filter").Any(),
		)
	}
	f.Line()

	// --- OrderField struct ---
	f.Comment(fmt.Sprintf("%s defines the ordering field of %s.", orderFieldName, t.Name))
	f.Type().Id(orderFieldName).Struct(
		jen.Id("Column").String(),
		jen.Id("ToCursor").Func().Params(jen.Op("*").Id(typeName)).Qual(gqlrelayPkg, "Cursor"),
	)
	f.Line()

	// --- Order struct ---
	f.Comment(fmt.Sprintf("%s defines the ordering of %s.", orderName, t.Name))
	f.Type().Id(orderName).Struct(
		jen.Id("Direction").Qual(gqlrelayPkg, "OrderDirection").Tag(map[string]string{"json": "direction"}),
		jen.Id("Field").Op("*").Id(orderFieldName).Tag(map[string]string{"json": "field"}),
	)
	f.Line()

	// --- DefaultOrder variable ---
	g.genModelDefaultOrder(f, t, typeName, receiver, orderName, orderFieldName, defaultOrderName)

	// --- OrderField variables for each orderable field ---
	g.genModelOrderFieldVars(f, t, typeName, receiver, orderFieldName)

	// --- OrderField methods (String, MarshalGQL, UnmarshalGQL) ---
	g.genModelOrderFieldMethods(f, t, typeName, orderFieldName)

	// --- ToEdge method on entity ---
	f.Comment(fmt.Sprintf("ToEdge converts %s into %s.", typeName, edgeName))
	f.Func().Params(
		jen.Id(receiver).Op("*").Id(typeName),
	).Id("ToEdge").Params(
		jen.Id("order").Op("*").Id(orderName),
	).Op("*").Id(edgeName).Block(
		jen.If(jen.Id("order").Op("==").Nil()).Block(
			jen.Id("order").Op("=").Id(defaultOrderName),
		),
		jen.Return(jen.Op("&").Id(edgeName).Values(
			jen.Id("Node").Op(":").Id(receiver),
			jen.Id("Cursor").Op(":").Id("order").Dot("Field").Dot("ToCursor").Call(jen.Id(receiver)),
		)),
	)
	f.Line()

	// --- WithOrder function ---
	g.genModelWithOrder(f, typeName, optName, orderName, pagerConfigName, defaultOrderName, multiOrder)

	// --- WithFilter function ---
	g.genModelWithFilter(f, typeName, optName, pagerConfigName)
}

// genModelDefaultOrder generates the DefaultOrder variable in the root package.
func (g *Generator) genModelDefaultOrder(f *jen.File, t *gen.Type, typeName, receiver, orderName, orderFieldName, defaultOrderName string) {
	if t.ID == nil {
		return
	}
	idField := "ID"
	if !strings.EqualFold(t.ID.Name, "id") {
		idField = pascal(t.ID.Name)
	}

	idStorageKey := t.ID.StorageKey()

	f.Comment(fmt.Sprintf("%s is the default ordering of %s.", defaultOrderName, t.Name))
	f.Var().Id(defaultOrderName).Op("=").Op("&").Id(orderName).Values(
		jen.Id("Direction").Op(":").Qual(gqlrelayPkg, "OrderDirectionAsc"),
		jen.Id("Field").Op(":").Op("&").Id(orderFieldName).Values(
			jen.Id("Column").Op(":").Lit(idStorageKey),
			jen.Id("ToCursor").Op(":").Func().Params(jen.Id(receiver).Op("*").Id(typeName)).Qual(gqlrelayPkg, "Cursor").Block(
				jen.Return(jen.Qual(gqlrelayPkg, "Cursor").Values(jen.Id("ID").Op(":").Id(receiver).Dot(idField))),
			),
		),
	)
	f.Line()
}

// genModelOrderFieldVars generates order field variables in the root package.
func (g *Generator) genModelOrderFieldVars(f *jen.File, t *gen.Type, typeName, receiver, orderFieldName string) {
	orderableFields := g.orderableFields(t)
	if len(orderableFields) == 0 {
		return
	}

	f.Var().DefsFunc(func(group *jen.Group) {
		for _, fld := range orderableFields {
			varName := camel(typeName) + "By" + pascal(fld.Name)
			structField := pascal(fld.Name)

			group.Comment(fmt.Sprintf("%s orders %s by %s.", varName, t.Name, fld.Name))
			storageKey := fld.StorageKey()
			group.Id(varName).Op("=").Op("&").Id(orderFieldName).Values(
				jen.Id("Column").Op(":").Lit(storageKey),
				jen.Id("ToCursor").Op(":").Func().Params(jen.Id(receiver).Op("*").Id(typeName)).Qual(gqlrelayPkg, "Cursor").BlockFunc(func(blk *jen.Group) {
					if fld.Nillable {
						// Nillable fields are pointers — dereference safely to avoid nil in cursor serialization.
						blk.Id("cv").Op(":=").Qual(gqlrelayPkg, "Cursor").Values(jen.Id("ID").Op(":").Id(receiver).Dot("ID"))
						blk.If(jen.Id(receiver).Dot(structField).Op("!=").Nil()).Block(
							jen.Id("cv").Dot("Value").Op("=").Op("*").Id(receiver).Dot(structField),
						)
						blk.Return(jen.Id("cv"))
					} else {
						blk.Return(jen.Qual(gqlrelayPkg, "Cursor").Values(
							jen.Id("ID").Op(":").Id(receiver).Dot("ID"),
							jen.Id("Value").Op(":").Id(receiver).Dot(structField),
						))
					}
				}),
			)
		}
	})
	f.Line()
}

// genModelOrderFieldMethods generates String, MarshalGQL, UnmarshalGQL methods
// for the OrderField type in the root package.
func (g *Generator) genModelOrderFieldMethods(f *jen.File, t *gen.Type, typeName, orderFieldName string) {
	orderableFields := g.orderableFields(t)

	// String method
	f.Comment("String implement fmt.Stringer interface.")
	f.Func().Params(jen.Id("f").Id(orderFieldName)).Id("String").Params().String().BlockFunc(func(group *jen.Group) {
		group.Var().Id("str").String()
		group.Switch(jen.Id("f").Dot("Column")).BlockFunc(func(sw *jen.Group) {
			for _, fld := range orderableFields {
				varName := camel(typeName) + "By" + pascal(fld.Name)
				gqlName := g.getOrderFieldName(fld)
				sw.Case(jen.Id(varName).Dot("Column")).Block(
					jen.Id("str").Op("=").Lit(gqlName),
				)
			}
			sw.Default().Block(
				jen.Qual("log/slog", "Warn").Call(
					jen.Lit("unknown "+orderFieldName+" column"),
					jen.Lit("column"), jen.Id("f").Dot("Column"),
				),
			)
		})
		group.Return(jen.Id("str"))
	})
	f.Line()

	// MarshalGQL method
	f.Comment("MarshalGQL implements graphql.Marshaler interface.")
	f.Func().Params(jen.Id("f").Id(orderFieldName)).Id("MarshalGQL").Params(
		jen.Id("w").Qual("io", "Writer"),
	).Block(
		jen.Qual("io", "WriteString").Call(jen.Id("w"), jen.Qual("strconv", "Quote").Call(jen.Id("f").Dot("String").Call())),
	)
	f.Line()

	// UnmarshalGQL method
	f.Comment("UnmarshalGQL implements graphql.Unmarshaler interface.")
	f.Func().Params(jen.Id("f").Op("*").Id(orderFieldName)).Id("UnmarshalGQL").Params(
		jen.Id("v").Any(),
	).Error().BlockFunc(func(group *jen.Group) {
		group.List(jen.Id("str"), jen.Id("ok")).Op(":=").Id("v").Assert(jen.String())
		group.If(jen.Op("!").Id("ok")).Block(
			jen.Return(jen.Qual("fmt", "Errorf").Call(jen.Lit(orderFieldName+" %T must be a string"), jen.Id("v"))),
		)
		group.Switch(jen.Id("str")).BlockFunc(func(sw *jen.Group) {
			for _, fld := range orderableFields {
				varName := camel(typeName) + "By" + pascal(fld.Name)
				gqlName := g.getOrderFieldName(fld)
				sw.Case(jen.Lit(gqlName)).Block(
					jen.Op("*").Id("f").Op("=").Op("*").Id(varName),
				)
			}
			sw.Default().Block(
				jen.Return(jen.Qual("fmt", "Errorf").Call(jen.Lit("%s is not a valid "+orderFieldName), jen.Id("str"))),
			)
		})
		group.Return(jen.Nil())
	})
	f.Line()
}

// genModelWithOrder generates the WithOrder function in the root package.
func (g *Generator) genModelWithOrder(f *jen.File, typeName, optName, orderName, pagerConfigName, defaultOrderName string, multiOrder bool) {
	optOrderName := "With" + typeName + "Order"
	f.Comment(fmt.Sprintf("%s configures pagination ordering.", optOrderName))

	if multiOrder {
		f.Func().Id(optOrderName).Params(
			jen.Id("order").Index().Op("*").Id(orderName),
		).Params(jen.Id(optName), jen.Error()).BlockFunc(func(grp *jen.Group) {
			grp.For(jen.List(jen.Id("_"), jen.Id("o")).Op(":=").Range().Id("order")).Block(
				jen.If(jen.Id("err").Op(":=").Id("o").Dot("Direction").Dot("Validate").Call(), jen.Id("err").Op("!=").Nil()).Block(
					jen.Return(jen.Nil(), jen.Id("err")),
				),
			)
			grp.Return(jen.Func().Params(jen.Id("cfg").Op("*").Id(pagerConfigName)).Block(
				jen.Id("cfg").Dot("Order").Op("=").Append(jen.Id("cfg").Dot("Order"), jen.Id("order").Op("...")),
			), jen.Nil())
		})
	} else {
		f.Func().Id(optOrderName).Params(
			jen.Id("order").Op("*").Id(orderName),
		).Id(optName).Block(
			jen.Return(jen.Func().Params(jen.Id("cfg").Op("*").Id(pagerConfigName)).Block(
				jen.If(jen.Id("order").Op("==").Nil()).Block(
					jen.Id("order").Op("=").Id(defaultOrderName),
				),
				jen.Id("cfg").Dot("Order").Op("=").Id("order"),
			)),
		)
	}
	f.Line()
}

// genModelWithFilter generates WithXxxFilter function.
// The filter is stored as `any` in PagerConfig to avoid circular imports
// (root package cannot import entity sub-packages or gqlfilter/).
// The entity's Paginate method type-asserts it to func(*XxxQuery) (*XxxQuery, error).
func (g *Generator) genModelWithFilter(f *jen.File, typeName, optName, pagerConfigName string) {
	optFilterName := "With" + typeName + "Filter"
	f.Commentf("%s sets a filter function for %s pagination.", optFilterName, typeName)
	f.Commentf("The filter parameter should be func(*%sQuery) (*%sQuery, error).", typeName, typeName)
	f.Func().Id(optFilterName).Params(
		jen.Id("filter").Any(),
	).Id(optName).Block(
		jen.Return(jen.Func().Params(jen.Id("cfg").Op("*").Id(pagerConfigName)).Block(
			jen.Id("cfg").Dot("Filter").Op("=").Id("filter"),
		)),
	)
	f.Line()
}

// genPaginatableInterface generates the Paginatable interface for an entity.
// Edge methods use type assertion to call Paginate on querier results:
//
//	p, ok := q.(entity.UserPaginatable)
//	return p.Paginate(ctx, after, first, before, last, opts...)
func (g *Generator) genPaginatableInterface(f *jen.File, t *gen.Type) {
	typeName := g.graphqlTypeName(t)
	ifaceName := typeName + "Paginatable"
	connName := typeName + "Connection"
	optName := typeName + "PaginateOption"

	f.Commentf("%s is implemented by query types that support Relay cursor pagination.", ifaceName)
	f.Type().Id(ifaceName).Interface(
		jen.Id("Paginate").Params(
			jen.Id("ctx").Qual("context", "Context"),
			jen.Id("after").Op("*").Qual(gqlrelayPkg, "Cursor"),
			jen.Id("first").Op("*").Int(),
			jen.Id("before").Op("*").Qual(gqlrelayPkg, "Cursor"),
			jen.Id("last").Op("*").Int(),
			jen.Id("opts").Op("...").Id(optName),
		).Params(
			jen.Op("*").Id(connName),
			jen.Error(),
		),
	)
}

