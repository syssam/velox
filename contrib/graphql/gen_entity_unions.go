package graphql

// Union marker generator: emits empty marker methods so velox entity types
// satisfy the marker interfaces gqlgen generates for `union X = A | B`.
// Output: entity/gql_unions.go (one shared file containing all markers).
//
// Velox does NOT emit the union SDL — the user declares
// `union X = A | B | ...` in their own .graphql file. We only generate the
// Go-side trait so the entity types satisfy gqlgen's `IsX()` interface.

import (
	"fmt"
	"slices"

	"github.com/dave/jennifer/jen"
)

// validateUnions collects union memberships across all entities and validates:
//   - empty union names are rejected
//   - each union must have ≥2 distinct member entities (GraphQL spec requirement)
//
// Returns a map of union-name -> sorted list of member entity names. Returns an
// empty map (not nil) and no error when no entity declares any UnionMember.
func (g *Generator) validateUnions() (map[string][]string, error) {
	members := make(map[string]map[string]struct{})
	for _, t := range g.graph.Nodes {
		ann := g.getTypeAnnotation(t)
		for _, name := range ann.GetUnions() {
			if name == "" {
				return nil, fmt.Errorf("entity %q: empty union name in graphql.UnionMember", t.Name)
			}
			if _, ok := members[name]; !ok {
				members[name] = make(map[string]struct{})
			}
			members[name][t.Name] = struct{}{}
		}
	}

	out := make(map[string][]string, len(members))
	for union, set := range members {
		if len(set) < 2 {
			return nil, fmt.Errorf(`union %q requires at least 2 member entities (got %d); GraphQL spec disallows single-member unions`, union, len(set))
		}
		names := make([]string, 0, len(set))
		for n := range set {
			names = append(names, n)
		}
		slices.Sort(names)
		out[union] = names
	}
	return out, nil
}

// genUnionsShared returns a *jen.File for entity/gql_unions.go listing all
// union marker methods, or nil when no entity declares any UnionMember.
//
// unionMembers maps union-name -> sorted list of member entity names; the
// caller (validateUnions) has already enforced ≥2 members per union.
func (g *Generator) genUnionsShared(unionMembers map[string][]string) *jen.File {
	if len(unionMembers) == 0 {
		return nil
	}

	// Invert: entity-name -> sorted list of union-names it belongs to.
	// We emit methods grouped by entity (so a reviewer sees all the markers
	// on User in one place) rather than by union.
	byEntity := make(map[string][]string, len(g.graph.Nodes))
	for union, members := range unionMembers {
		for _, e := range members {
			byEntity[e] = append(byEntity[e], union)
		}
	}
	for e := range byEntity {
		slices.Sort(byEntity[e])
	}

	entityNames := make([]string, 0, len(byEntity))
	for e := range byEntity {
		entityNames = append(entityNames, e)
	}
	slices.Sort(entityNames)

	f := jen.NewFilePathName(g.config.ORMPackage+"/entity", "entity")
	f.HeaderComment("Code generated by velox. DO NOT EDIT.")
	f.Comment("Marker methods for GraphQL union membership.")
	f.Comment("Each empty method satisfies the gqlgen-generated `IsXxx()` interface")
	f.Comment("for `union Xxx = ...` declared in the user's .graphql schema.")

	for _, name := range entityNames {
		for _, union := range byEntity[name] {
			f.Func().
				Params(jen.Op("*").Id(name)).
				Id("Is" + union).
				Params().
				Block()
		}
	}

	return f
}
