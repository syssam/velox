// TestQuery_LimitOrderDesc verifies Limit + Order desc.
func TestQuery_LimitOrderDesc(t *testing.T) {
	client := openTestClient(t)
	ctx := context.Background()

	createUser(t, client, "Alice", "a@test.com")
	createUser(t, client, "Bob", "b@test.com")
	createUser(t, client, "Charlie", "c@test.com")

	users, err := client.User.Query().
		Order(user.ByName(sql.OrderDesc())).
		Limit(2).
		All(ctx)
	require.NoError(t, err)
	require.Len(t, users, 2)
	assert.Equal(t, "Charlie", users[0].Name)
	assert.Equal(t, "Bob", users[1].Name)
}

