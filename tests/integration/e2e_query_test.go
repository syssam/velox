// TestQuery_TokenIDs verifies IDs(), FirstID(), and OnlyID() work on a
// UUID-keyed entity. These methods use runtime.ExtractID which previously
// panicked on non-integer ID types (Bug B1).
func TestQuery_TokenIDs(t *testing.T) {
	client := openTestClient(t)
	ctx := context.Background()

	// Create two tokens (UUID PKs are generated by Default(uuid.New)).
	tok1, err := client.Token.Create().SetName("tok-alpha").Save(ctx)
	require.NoError(t, err)
	tok2, err := client.Token.Create().SetName("tok-beta").Save(ctx)
	require.NoError(t, err)

	// IDs — should return both UUIDs.
	ids, err := client.Token.Query().IDs(ctx)
	require.NoError(t, err)
	require.Len(t, ids, 2)
	assert.ElementsMatch(t, []uuid.UUID{tok1.ID, tok2.ID}, ids)

	// FirstID — should return one of the two (order not guaranteed without Order).
	firstID, err := client.Token.Query().FirstID(ctx)
	require.NoError(t, err)
	assert.Contains(t, []uuid.UUID{tok1.ID, tok2.ID}, firstID)

	// OnlyID — needs exactly one match, so filter.
	onlyID, err := client.Token.Query().Where(
		predicate.Token(sql.FieldEQ("name", "tok-alpha")),
	).OnlyID(ctx)
	require.NoError(t, err)
	assert.Equal(t, tok1.ID, onlyID)
}
